'use strict';

//арифметическая операция +
function SumOperation(arg1, arg2) {
    return (arg1 + arg2);
}

//арифметическая операция -
function DiffOperation(arg1, arg2) {
    return (arg1 - arg2);
}

//арифметическая операция *
function MultiOperation(arg1, arg2) {
    return (arg1 * arg2);
}

//арифметическая операция /
function DivOperation(arg1, arg2) {
    return (arg1 / arg2);
}
